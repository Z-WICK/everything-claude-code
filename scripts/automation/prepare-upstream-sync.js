#!/usr/bin/env node
/**
 * Prepare an automation branch that merges the upstream repository into this fork,
 * then refreshes and validates the Factory sidecar before opening or updating a PR.
 */

const fs = require('fs');
const path = require('path');
const { spawnSync } = require('child_process');

const ROOT_DIR = path.join(__dirname, '..', '..');

const DEFAULTS = {
  baseBranch: 'main',
  syncBranch: 'automation/upstream-sync',
  upstreamRemote: 'upstream',
  upstreamRepo: 'affaan-m/everything-claude-code',
  upstreamBranch: 'main',
  gitUserName: 'github-actions[bot]',
  gitUserEmail: '41898282+github-actions[bot]@users.noreply.github.com',
  sidecarRefreshCommitMessage: 'chore: refresh factory sidecar after upstream sync',
};

function assertMatch(value, pattern, label) {
  if (!pattern.test(value)) {
    throw new Error(`Invalid ${label}: ${value}`);
  }
}

function resolveSyncConfig(env = process.env) {
  const config = {
    baseBranch: env.UPSTREAM_SYNC_BASE_BRANCH || DEFAULTS.baseBranch,
    syncBranch: env.UPSTREAM_SYNC_BRANCH || DEFAULTS.syncBranch,
    upstreamRemote: env.UPSTREAM_REMOTE || DEFAULTS.upstreamRemote,
    upstreamRepo: env.UPSTREAM_REPO || DEFAULTS.upstreamRepo,
    upstreamBranch: env.UPSTREAM_BRANCH || DEFAULTS.upstreamBranch,
    gitUserName: env.GIT_COMMITTER_NAME || env.GIT_AUTHOR_NAME || DEFAULTS.gitUserName,
    gitUserEmail: env.GIT_COMMITTER_EMAIL || env.GIT_AUTHOR_EMAIL || DEFAULTS.gitUserEmail,
    sidecarRefreshCommitMessage: env.SIDECAR_REFRESH_COMMIT_MESSAGE || DEFAULTS.sidecarRefreshCommitMessage,
  };

  assertMatch(config.baseBranch, /^[A-Za-z0-9._/-]+$/, 'base branch');
  assertMatch(config.syncBranch, /^[A-Za-z0-9._/-]+$/, 'sync branch');
  assertMatch(config.upstreamRemote, /^[A-Za-z0-9._-]+$/, 'upstream remote');
  assertMatch(config.upstreamRepo, /^[A-Za-z0-9_.-]+\/[A-Za-z0-9_.-]+$/, 'upstream repo');
  assertMatch(config.upstreamBranch, /^[A-Za-z0-9._/-]+$/, 'upstream branch');

  return config;
}

function runCommand(command, args, options = {}) {
  const result = spawnSync(command, args, {
    cwd: ROOT_DIR,
    encoding: 'utf8',
    stdio: ['ignore', 'pipe', 'pipe'],
    ...options,
  });

  if (result.error) {
    throw result.error;
  }

  if (result.status !== 0) {
    const output = [result.stdout, result.stderr]
      .filter(Boolean)
      .join('\n')
      .trim();

    throw new Error(
      `${command} ${args.join(' ')} failed with exit ${result.status}${output ? `\n${output}` : ''}`,
    );
  }

  return {
    stdout: (result.stdout || '').trim(),
    stderr: (result.stderr || '').trim(),
  };
}

function ensureRemote(name, repoSlug) {
  const remoteUrl = `https://github.com/${repoSlug}.git`;
  const remotes = runCommand('git', ['remote']).stdout.split('\n').filter(Boolean);

  if (remotes.includes(name)) {
    runCommand('git', ['remote', 'set-url', name, remoteUrl]);
    return remoteUrl;
  }

  runCommand('git', ['remote', 'add', name, remoteUrl]);
  return remoteUrl;
}

function getRevParse(ref) {
  return runCommand('git', ['rev-parse', ref]).stdout;
}

function getAheadCount(baseRef) {
  const raw = runCommand('git', ['rev-list', '--count', `${baseRef}..HEAD`]).stdout;
  return Number(raw || '0');
}

function hasWorktreeChanges() {
  return runCommand('git', ['status', '--short']).stdout.length > 0;
}

function commitSidecarRefresh(message) {
  runCommand('git', ['add', '-A', '.factory']);

  const staged = runCommand('git', ['diff', '--cached', '--name-only']).stdout;
  if (!staged) {
    return false;
  }

  runCommand('git', ['commit', '-m', message]);
  return true;
}

function buildPullRequestMetadata({
  upstreamRepo,
  upstreamBranch,
  baseBranch,
  syncBranch,
  baseSha,
  upstreamSha,
  headSha,
}) {
  const shortSha = (value) => value.slice(0, 7);
  const title = `chore: sync upstream ${upstreamBranch} into ${baseBranch}`;
  const body = [
    '## Automated Upstream Sync',
    '',
    `This PR was generated automatically from \`${syncBranch}\`.`,
    '',
    '### What changed',
    '',
    `- Merged \`${upstreamRepo}@${upstreamBranch}\` into \`${baseBranch}\``,
    '- Re-ran `.factory/_ecc/sync.js` to refresh the Factory sidecar mirrors',
    '- Re-ran `.factory/_ecc/validate.js` to confirm the generated mirrors stay in sync',
    '',
    '### Revision details',
    '',
    `- Base SHA: \`${shortSha(baseSha)}\``,
    `- Upstream SHA: \`${shortSha(upstreamSha)}\``,
    `- Prepared branch head: \`${shortSha(headSha)}\``,
    '',
    '### Merge notes',
    '',
    '- If this PR is green, merge it normally into `main`.',
    '- If the upstream merge conflicts in automation, resolve the conflict locally and rerun the workflow.',
    '',
    '_Generated by `.github/workflows/upstream-sync.yml`._',
  ].join('\n');

  return { title, body };
}

function appendGitHubOutputs(filePath, outputs) {
  if (!filePath) {
    return;
  }

  for (const [key, value] of Object.entries(outputs)) {
    const serialized = String(value);
    const delimiter = `ECC_${key.toUpperCase()}_${Date.now()}_${Math.random().toString(36).slice(2, 8)}`;
    fs.appendFileSync(filePath, `${key}<<${delimiter}\n${serialized}\n${delimiter}\n`);
  }
}

function setGitHubOutputs(outputs) {
  appendGitHubOutputs(process.env.GITHUB_OUTPUT, outputs);
}

function prepareUpstreamSync(env = process.env) {
  const config = resolveSyncConfig(env);
  const baseRef = `origin/${config.baseBranch}`;
  const upstreamRef = `${config.upstreamRemote}/${config.upstreamBranch}`;

  runCommand('git', ['config', 'user.name', config.gitUserName]);
  runCommand('git', ['config', 'user.email', config.gitUserEmail]);
  ensureRemote(config.upstreamRemote, config.upstreamRepo);

  runCommand('git', ['fetch', 'origin', config.baseBranch]);
  runCommand('git', ['fetch', config.upstreamRemote, config.upstreamBranch]);

  const baseSha = getRevParse(baseRef);
  const upstreamSha = getRevParse(upstreamRef);

  runCommand('git', ['checkout', '-B', config.syncBranch, baseRef]);

  try {
    runCommand('git', ['merge', '--no-ff', '--no-edit', upstreamRef]);
  } catch (error) {
    try {
      runCommand('git', ['merge', '--abort']);
    } catch {
      // Ignore abort failures when git never entered a merge state.
    }

    throw new Error(
      `Unable to merge ${upstreamRef} into ${config.syncBranch}. Resolve the conflict manually.\n${error.message}`,
    );
  }

  runCommand(process.execPath, ['.factory/_ecc/sync.js']);
  runCommand(process.execPath, ['.factory/_ecc/validate.js']);

  if (hasWorktreeChanges()) {
    commitSidecarRefresh(config.sidecarRefreshCommitMessage);
  }

  const headSha = getRevParse('HEAD');
  const aheadCount = getAheadCount(baseRef);
  const hasChanges = aheadCount > 0;
  const metadata = buildPullRequestMetadata({
    upstreamRepo: config.upstreamRepo,
    upstreamBranch: config.upstreamBranch,
    baseBranch: config.baseBranch,
    syncBranch: config.syncBranch,
    baseSha,
    upstreamSha,
    headSha,
  });

  return {
    ...config,
    baseSha,
    upstreamSha,
    headSha,
    aheadCount,
    hasChanges,
    prTitle: metadata.title,
    prBody: metadata.body,
  };
}

function main() {
  const result = prepareUpstreamSync(process.env);

  setGitHubOutputs({
    base_branch: result.baseBranch,
    sync_branch: result.syncBranch,
    upstream_repo: result.upstreamRepo,
    upstream_branch: result.upstreamBranch,
    base_sha: result.baseSha,
    upstream_sha: result.upstreamSha,
    head_sha: result.headSha,
    ahead_count: String(result.aheadCount),
    has_changes: String(result.hasChanges),
    pr_title: result.prTitle,
    pr_body: result.prBody,
  });

  console.log(JSON.stringify({
    baseBranch: result.baseBranch,
    syncBranch: result.syncBranch,
    upstreamRepo: result.upstreamRepo,
    upstreamBranch: result.upstreamBranch,
    baseSha: result.baseSha,
    upstreamSha: result.upstreamSha,
    headSha: result.headSha,
    aheadCount: result.aheadCount,
    hasChanges: result.hasChanges,
  }, null, 2));
}

if (require.main === module) {
  main();
}

module.exports = {
  DEFAULTS,
  appendGitHubOutputs,
  buildPullRequestMetadata,
  prepareUpstreamSync,
  resolveSyncConfig,
};
